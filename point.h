#ifndef POINT_H
#define POINT_H

/**
 * @struct Point
 * @brief A structure to represent a point in 2D space.
 *
 * This structure holds the coordinates of a point in a 2D plane.
 * It contains two integer members:
 * - x: The x-coordinate of the point.
 * - y: The y-coordinate of the point.
 */
typedef struct Point {
    int x;
    int y;
} Point;


#endif